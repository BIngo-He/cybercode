"""
=====================================================================
bus.py - 异步任务队列
=====================================================================
主要功能：
提供跨协程的任务队列，用于解耦用户输入和 Agent 处理
=====================================================================
"""

# ==================== 导入部分 ====================
import asyncio


# =====================================================================
# task_queue - 全局异步任务队列
# =====================================================================
# 用户输入协程 (user_input_loop) 将用户消息放入队列
# Agent 工作协程 (agent_worker) 从队列取出消息处理
# 这样实现了输入和处理 的解耦
task_queue = asyncio.Queue()

# =====================================================================
# emit_task() - 向任务队列发送任务
# =====================================================================
# 参数：content - 任务内容（用户输入的文本）
async def emit_task(content: str):
    await task_queue.put(content)